<h2>I am getting error `Your account is active from 2 locations. Login again to discard session`.</h2>
<p>In <a href='http://localhost/my/account.php'>Account setting</a> there is an option of Max-Logins which tells the server that from how many devices you can be active simultaneously. If you cross Max-Logins limit you will see this Error Message. You can discard one of the active sessions from other devices by logging in.</p>
