<h2>Difference between Free print and Paid Print.</h2>
<p>Free print and Paid print are Print Modes. This tells the server how to debit your Orntel account for print task.
If your print mode is `free`, coins will debit from free points and if you choose `paid` then coins will debit from your purchased credits.</p>
